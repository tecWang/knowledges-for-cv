import heapq

nums = [-132, -23, -54, -1, -5, -3, -2, 2, 3, 5, 1, 54, 23, 132]
heap = []
heap2 = []
for n in nums:
    heapq.heappush(heap, n)

print(heap)     # [-132, -23, -54, -1, -5, -3, -2, 2, 3, 5, 1, 54, 23, 132]
while heap:
    print(heapq.heappop(heap), end=", ")
    # -132, -54, -23, -5, -3, -2, -1, 1, 2, 3, 5, 23, 54, 132,

# 可以看到heap直接打印出来的结果貌似不是我们期望的排序结果
# 那是因为heapq其实是依赖于数组建立了一个堆，因为打印出来的结果其实是堆的结果，自然是无序的
# 当初始数组完全是正数时，结果看起来是正确的，大约只是个巧合吧~
# 使用heapq的heappop方法就可以正常的打印出排序后的结果了